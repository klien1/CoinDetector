package com.example.android.coindetector;

public class TextObject {
    public float x;
    public float y;
    public String text;
    public String color;

    TextObject() { }

    float getX() {
        return this.x;
    }

    float getY() {
        return this.y;
    }

    String getText() {
        return this.text;
    }

    String getColor() {
        return this.color;
    }
}
